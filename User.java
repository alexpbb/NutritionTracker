public class User {
    private String fName;
    private String lName;
    private String sex;
    private int age;
    private int height;
    private double weight;

    public User(String fName, String lName, String sex, int age, int height, double weight) {
        this.fName = fName;
        this.lName= lName;
        this.sex = sex;
        this.age = age;
        this.height = height;
        this.weight = weight;
    }

    

}
